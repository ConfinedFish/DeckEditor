package test;

public class ColorIdentity{
	private boolean w;
	private boolean b;
	private boolean u;
	private boolean r;
	private boolean g;
	private boolean c;
	
	public ColorIdentity(boolean w, boolean b, boolean u, boolean r, boolean g, boolean c){
		this.w = w;
		this.b = b;
		this.u = u;
		this.r = r;
		this.g = g;
		this.c = c;
	}
	
	public boolean isW(){
		return w;
	}
	
	public boolean isB(){
		return b;
	}
	
	public boolean isU(){
		return u;
	}
	
	public boolean isR(){
		return r;
	}
	
	public boolean isG(){
		return g;
	}
	
	public boolean isC(){
		return c;
	}
}
